-- ── RLS TEST TABLE ──
-- Used for RLS policy tests (#622-#630). Has restrictive policies that
-- demonstrate auth.uid(), auth.role(), auth.email(), auth.jwt() usage.
DROP TABLE IF EXISTS rls_todos CASCADE;
CREATE TABLE rls_todos (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    user_id TEXT NOT NULL,
    email TEXT,
    role TEXT DEFAULT 'user',
    title TEXT NOT NULL,
    completed BOOLEAN DEFAULT false
);
INSERT INTO rls_todos (user_id, email, role, title, completed) VALUES
    ('test-user-1', 'owner@example.com', 'admin', 'Admin todo', false),
    ('test-user-2', 'user@example.com', 'user', 'User todo', false),
    ('test-user-3', 'viewer@example.com', 'viewer', 'Viewer todo', true);

ALTER TABLE rls_todos ENABLE ROW LEVEL SECURITY;

-- RLS policy tests use specific policies:
-- policy-select: authenticated users can SELECT
-- policy-insert: users can INSERT (owner policy)
-- policy-update: only owner can UPDATE
-- policy-delete: only admin role can DELETE
-- auth.uid: rows filtered by auth.uid()
-- auth.role: access based on role
-- auth.email: access based on email in JWT
-- auth.jwt: custom claims access

-- Default: no access (explicit policies below grant access)
-- SELECT: authenticated can see all
CREATE POLICY "rls_todos select" ON rls_todos FOR SELECT
    TO authenticated
    USING (true);

-- INSERT: any authenticated user can insert
CREATE POLICY "rls_todos insert" ON rls_todos FOR INSERT
    TO authenticated
    WITH CHECK (true);

-- UPDATE: only the owner (matching user_id) can update
CREATE POLICY "rls_todos update" ON rls_todos FOR UPDATE
    TO authenticated
    USING (auth.uid()::text = user_id);

-- DELETE: only the row owner can delete
CREATE POLICY "rls_todos delete" ON rls_todos FOR DELETE
    TO authenticated
    USING (auth.uid()::text = user_id);

-- Note: anon role has NO access to rls_todos (no anon policies).
-- This lets us test that anon gets blocked while authenticated works.

