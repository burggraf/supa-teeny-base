-- ── STORAGE RLS POLICIES ──
CREATE POLICY "Public buckets are viewable by everyone"
    ON storage.buckets FOR SELECT USING (true);
CREATE POLICY "Users can insert buckets (authenticated)"
    ON storage.buckets FOR INSERT TO authenticated WITH CHECK (true);
CREATE POLICY "Users can update buckets (authenticated)"
    ON storage.buckets FOR UPDATE TO authenticated USING (true);
CREATE POLICY "Users can delete buckets (authenticated)"
    ON storage.buckets FOR DELETE TO authenticated USING (true);

CREATE POLICY "Give public read access on public buckets"
    ON storage.objects FOR SELECT
    USING ((SELECT public FROM storage.buckets WHERE id = bucket_id) = true);
CREATE POLICY "Give public write access on public buckets"
    ON storage.objects FOR INSERT
    WITH CHECK ((SELECT public FROM storage.buckets WHERE id = bucket_id) = true);
CREATE POLICY "Authenticated users can upload"
    ON storage.objects FOR INSERT TO authenticated WITH CHECK (true);
CREATE POLICY "Authenticated users can update own files"
    ON storage.objects FOR UPDATE TO authenticated USING (true);
CREATE POLICY "Authenticated users can delete own files"
    ON storage.objects FOR DELETE TO authenticated USING (true);
CREATE POLICY "Authenticated users can read objects"
    ON storage.objects FOR SELECT TO authenticated USING (true);
CREATE POLICY "Anon can download public files"
    ON storage.objects FOR SELECT TO anon
    USING ((SELECT public FROM storage.buckets WHERE id = bucket_id) = true);
